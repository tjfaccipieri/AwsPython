#Listar números a partir de uma variavel, e qual a tipagem do numero

inteiro = 2
print(inteiro)
print(type(inteiro))
print(str(inteiro) + "é do tipo " + str(type(inteiro)))

ponto_flutuante = 3.14
print(ponto_flutuante)
print(type(ponto_flutuante))
print(str(ponto_flutuante) + "é do tipo " + str(type(ponto_flutuante)))

complexo = 2j
print(complexo)
print(type(complexo))
print(str(complexo) + "é do tipo " + str(type(complexo)))

verdadeiro = True
print(verdadeiro)
print(type(verdadeiro))
print(str(verdadeiro) + "é do tipo " + str(type(verdadeiro)))

falso = False
print(falso)
print(type(falso))
print(str(falso) + "é do tipo " + str(type(falso)))
